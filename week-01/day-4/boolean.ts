'use strict';

// True, false statements
console.log(true);
console.log(false);

// Negation
console.log(!true);
console.log(!false);

// And
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);

// Or
console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);

